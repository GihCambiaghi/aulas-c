//Álgebra dos Inteiros V = 4/3 TTr³

#include <stdio.h>

int main()
{
    int r = 1.0, v;

// Um dos numeros tem que ser real e o outro é inteiro (abaixo)

    v = 4 * 3.1416 * r * r * r / 3;

// O número que por após o ponto, arredonda o valor para casa mensionada

    printf("V = %d\n", v);

    return 0;
}
